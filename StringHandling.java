/*
Problem Statement:
Create a program that demonstrates common string operations.

Explanation:
Strings are used to store text and support many built-in methods.
*/
public class StringHandling {
    public static void main(String[] args) {
        String name = "Mano";
        System.out.println(name.toUpperCase());
        System.out.println(name.length());
        System.out.println(name.concat(" Kumar"));
    }
}
