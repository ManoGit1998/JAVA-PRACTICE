/*
Problem Statement:
Create a program that stores and prints different types of values such as age, salary, grade, and whether a student is active.

Explanation:
Java has primitive data types like int, double, char, and boolean. These are used to store simple values.
*/
public class VariablesAndDataTypes {
    public static void main(String[] args) {
        int age = 25;
        double salary = 45000.50;
        char grade = 'A';
        boolean isStudent = true;

        System.out.println("Age: " + age);
        System.out.println("Salary: " + salary);
        System.out.println("Grade: " + grade);
        System.out.println("Is Student: " + isStudent);
    }
}
